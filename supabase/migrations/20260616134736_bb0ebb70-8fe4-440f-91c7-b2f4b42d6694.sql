-- 3) nps_pesquisas.empresa_id
ALTER TABLE public.nps_pesquisas
  ADD COLUMN empresa_id integer REFERENCES public.empresas(id) ON DELETE SET NULL;

CREATE INDEX idx_nps_pesquisas_empresa_id ON public.nps_pesquisas(empresa_id);

UPDATE public.nps_pesquisas n
SET empresa_id = e.id
FROM public.empresas e
WHERE n.empresa_id IS NULL
  AND n.empresa IS NOT NULL
  AND (
    lower(btrim(e.razao_social)) = lower(btrim(n.empresa))
    OR lower(btrim(e.titulo)) = lower(btrim(n.empresa))
  );
